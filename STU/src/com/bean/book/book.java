package com.bean.book;

public class book {
	private String tno;
	private String bname;
	private String  writer;
	private String  pubdept;
	private float  price;
	private Integer typnum;
	private String remark;
	public String getTno() {
		return tno;
	}
	public void setTno(String tno) {
		this.tno = tno;
	}
	public String getBname() {
		return bname;
	}
	public void setBname(String bname) {
		this.bname = bname;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public String getPubdept() {
		return pubdept;
	}
	public void setPubdept(String pubdept) {
		this.pubdept = pubdept;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public Integer getTypnum() {
		return typnum;
	}
	public void setTypnum(Integer typnum) {
		this.typnum = typnum;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}


}
